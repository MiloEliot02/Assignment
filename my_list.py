# A list of numbers
my_list = [10, 20, 30, 40]
# Insert 15 at the second position
my_list.insert(1, 15)
# Remove the last element
my_list.pop()
# Sort the list in ascending order
my_list.sort()
# Find and print the index of the list
index_of_30 = my_list.index(30)
# Print the list
print(index_of_30)